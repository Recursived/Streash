package streash.console;

import javax.swing.JTextField;
import javax.swing.text.Document;

public class ConsoleInput extends JTextField {

	public ConsoleInput() {
		super();
	}

	public ConsoleInput(Document arg0, String arg1, int arg2) {
		super(arg0, arg1, arg2);
	}

	public ConsoleInput(int arg0) {
		super(arg0);
	}

	public ConsoleInput(String arg0, int arg1) {
		super(arg0, arg1);
	}

	public ConsoleInput(String arg0) {
		super(arg0);
	}

}
